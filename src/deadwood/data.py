"""The world: a believable frontier company seeded into SQLite — and now an
edge-case-rich one, so a tool that walks it meets the data a *real* dump hits.

Deadwood Telegraph & Trust Co. — a bank and telegraph office. Employees, customers,
accounts, transactions, internal telegrams, an audit log, API keys and live staff
sessions, all generated by a small seeded faker (stdlib only) so the data looks
real and is the same every time. The company's older paper sits in a second,
ATTACHed ledger (`archive`) — a separate database on the same connection.

What makes it a realistic *target* — and exactly where a tool's edge-case bugs
surface when it walks the data out:

  * **NULL cells in dumpable tables.** A dump concatenates each row; one NULL cell
    makes the whole row NULL, and `group_concat` skips NULLs — so that row silently
    vanishes. A tool that doesn't coalesce loses rows and never notices.
  * **`group_concat` modelled like MySQL** (see `_GroupConcat`): NULLs skipped and
    the result capped at `group_concat_max_len` (1024 bytes). A table bigger than
    that truncates mid-dump, with no error — a whole-table read loses the tail.
  * **values longer than 32 chars, tables over 1024 bytes** — to exercise chunked /
    error-window extraction (extractvalue truncates ~32 chars per read).
  * **unicode / non-ASCII values** — to exercise an ASCII-first extractor's fallback.
  * **a second ATTACHed database** — cross-database enumeration.

Levels read from this shared world and seed their own secrets on top.
"""

